package org.Java.Self.Study;

public class JavaShortCut {
	public static void main(String[] args) {
//	ctrl+shift+L 단축키
//	ctrl+ +,- 폰트크기 증가/감소
//	ctrl+D 한줄 삭제
//	ctrl+alt+shift+Down 행단위 복사
//	alt+shift+A 멀티컬럼 편집
//	alt+up, down 행단위 이동
//	ctrl+i 자동 들여쓰기
//	ctrl+/ 주석 토글
//	ctrl+space 자동완성
//	ctrl+F11 실행
		
	System.out.println("단축키 모음입니다.");
	System.out.println("ctrl+shift+L 단축키");
	System.out.println("ctrl+ +,- 폰트크기 증가/감소");
	System.out.println("ctrl+D 한줄 삭제");
	System.out.println("ctrl+alt+shift+Down 행단위 복사");
	System.out.println("alt+shift+A 멀티컬럼 편집");
	System.out.println("alt+up, down 행단위 이동");
	System.out.println("ctrl+i 자동 들여쓰기");
	System.out.println("ctrl+/ 주석 토글");
	System.out.println("ctrl+space 자동완성");
	System.out.println("ctrl+F11 실행");
		

		
	}
}